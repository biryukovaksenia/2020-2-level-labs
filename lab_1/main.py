import re

def tokenize(text: str) -> list:
    try:
        text = text.lower()
        match = re.findall(r"\w+-\w+", text)
        match_new = []

        for i in match:
            new = i.replace('-', '')
            text = text.replace(i, new)

        match2 = re.findall(r"\w+'\w", text)

        for i in match2:
            new2 = i.replace("'", '')
            text = text.replace(i, new2)

        text_clean = re.findall(r'\w+', text)
        tokens = []

        for i in text_clean:
            if i.isalpha():
                tokens.append(i)
    except AttributeError:
        tokens = []

    return tokens



